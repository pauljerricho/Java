package Operator;

public class OperatorEx {
    // 메서드 생성
    public void operatorEx1(int a, int b){
        int result = a + b;
        System.out.println("덧셈 결과는 : " + result);
    }

    public void operatorEx2(int a, int b){
        int result = a - b;
        System.out.println("뻴셈 결과는 : " + result);
    }

    public void operatorEx3(int a, int b){
        int result = a * b;
        System.out.println("곱셈 결과는 : " + result);
    }

    public void operatorEx4(int a, int b){
        int result = a / b;
        System.out.println("나눗셈 결과는 : " + result);
    }
}
